    private void btnPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretragaActionPerformed
        // TODO add your handling code here:
        try {
            String rbStolaTekst = txtRB.getText().trim();
            Sto sto = new Sto();
            
            if(rbStolaTekst.isEmpty()){
                JOptionPane.showMessageDialog(this, "Unesite parametar za pretragu!");
                return;
            }
            if(!rbStolaTekst.matches("\\d+")){
                JOptionPane.showMessageDialog(this, "Niste uneli isključivo cifre!");
                return;
            }
            
            int brojStola = Integer.parseInt(rbStolaTekst);
            sto.setBrojStola(brojStola);
            
            
            btnPonisti.setEnabled(true);
            btnIzadji.setEnabled(true);
            btnDetalji.setEnabled(true);
            btnIzaberi.setEnabled(true);
            
            List <Sto> lista = Controller.getInstance().searchTables(sto);

            
            popuniTabelu(lista);
            JOptionPane.showMessageDialog(this, "Sistem je našao sto prema zadatoj vrednosti!","Pretraga stola", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe sto prema zadatoj vrednosti!","Pretraga stola", JOptionPane.INFORMATION_MESSAGE);
            podesiDugmice();
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnPretragaActionPerformed

