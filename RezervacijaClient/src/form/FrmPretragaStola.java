    private void popuniTabelu() {
        btnPonisti.setEnabled(false);
        btnPretraga.setEnabled(true);
        btnDetalji.setEnabled(false);
        btnIzaberi.setEnabled(false);
        
        txtRB.setText(" ");
        
        try {
            List<Sto> lista = Controller.getInstance().getAllTables();   
            ModelTabeleStolovi mts = new ModelTabeleStolovi(lista);
            tblSto.setModel(mts);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Niste popunili tabelu");
            ex.printStackTrace();
        }
        
        
    }

    private void popuniTabelu(List<Sto> lista) {
        try {
             ModelTabeleStolovi mts = new ModelTabeleStolovi();
             mts.refresh(lista);
            tblSto.setModel(mts);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Niste popunili tabelu");
            ex.printStackTrace();
        }
    }

    private void podesiDugmice() {
        btnPonisti.setEnabled(false);
      
    }
