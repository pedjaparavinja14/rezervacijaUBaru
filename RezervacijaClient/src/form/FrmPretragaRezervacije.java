    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        
        int red = tblRezervacije.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne moŽe da uČita podatke o odabranoj rezervaciji", "GreŠka", JOptionPane.ERROR_MESSAGE);
            return;

        } else {
            JOptionPane.showMessageDialog(this, "Sistem je ucitao rezervaciju");
            ModelTabeleRezervacije mtr = (ModelTabeleRezervacije) tblRezervacije.getModel();
            Rezervacija r = mtr.getRezervacija(red);
            System.out.println(r);
//            FrmIzmeniStavkuRez frmIzmeniStavku=new FrmIzmeniStavkuRez(r);
//            frmIzmeniStavku.setVisible(true);
             new FrmKreirajRezervaciju(r).setVisible(true);
        }
        
        
    }//GEN-LAST:event_btnDetaljiActionPerformed

    private void popuniTabelu() {
        btnPonisti.setEnabled(false);
        btnDetalji.setEnabled(false);
        
        try {
            List <Rezervacija> listaRezervacija = Controller.getInstance().getAllReservations();
            System.out.println(listaRezervacija);
            ModelTabeleRezervacije mtr = new ModelTabeleRezervacije(listaRezervacija);
            tblRezervacije.setModel(mtr);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }

