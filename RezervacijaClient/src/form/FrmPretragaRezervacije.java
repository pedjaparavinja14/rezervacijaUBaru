    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        // TODO add your handling code here:
        try {
            String brRezStr = txtBrojRezervacije.getText().trim();
            Rezervacija rez = new Rezervacija();
            
            if(brRezStr.isEmpty()){
                JOptionPane.showMessageDialog(this, "Unesite parametar za pretragu!");
                return;
            }
            
            if(!brRezStr.matches("\\d+")){
                JOptionPane.showMessageDialog(this, "Niste uneli isključivo cifre!");
                return;
            }
            
            int brojRezervacije = Integer.parseInt(brRezStr);
            
            btnPonisti.setEnabled(true);
            btnDetalji.setEnabled(true);
            
            rez.setBrojRezervacije(brojRezervacije);
            
            List <Rezervacija> lista = Controller.getInstance().searchReservations(rez);
            popuniTabelu(lista);
            JOptionPane.showMessageDialog(this, "Sistem je našao rezervacije prema zadatoj vrednosti!","Pretraga rezervacije", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe rezervacije prema zadatoj vrednosti!","Pretraga rezervacije", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

